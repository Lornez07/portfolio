#!/bin/bash
# GitHub Publishing Commands
# Copy and paste these commands into your terminal after creating your GitHub repository

echo "=== STEP 1: Create your GitHub repository first ==="
echo "1. Go to https://github.com"
echo "2. Click '+' -> 'New repository'"
echo "3. Name it 'portfolio-website' (or your preferred name)"
echo "4. Set to Public"
echo "5. DO NOT initialize with README/.gitignore/license"
echo "6. Click 'Create repository'"
echo ""

echo "=== STEP 2: Replace 'your-username' with your actual GitHub username ==="
echo "Then run these commands:"
echo ""

echo "git remote add origin https://github.com/your-username/portfolio-website.git"
echo "git branch -M main"
echo "git push -u origin main"
echo ""

echo "=== STEP 3: Enable GitHub Pages ==="
echo "1. Go to your repository on GitHub"
echo "2. Click 'Settings' -> 'Pages'"
echo "3. Source: 'Deploy from a branch'"
echo "4. Branch: 'main'"
echo "5. Click 'Save'"
echo ""

echo "=== STEP 4: Your site will be live at ==="
echo "https://your-username.github.io/portfolio-website"
echo ""

echo "=== IMPORTANT: Update these files after publishing ==="
echo "1. Update README.md with your actual GitHub Pages URL"
echo "2. Update index.html contact section if needed"
echo "3. Consider adding a custom domain"
echo ""

echo "Your portfolio website will be live! 🚀"